package de.comline.Model;

public class VSnapshot {

    private String vSnapshotVMName;
    private String vSnapshotVISDKServer;
    private String vSnapshotHost;
    private String import_date;
    private int upload_index;

    // Getter und Setter für vSnapshotVMName
    public String getVSnapshotVMName() {
        return vSnapshotVMName;
    }

    public void setVSnapshotVMName(String vSnapshotVMName) {
        this.vSnapshotVMName = vSnapshotVMName;
    }

    // Getter und Setter für vSnapshotVISDKServer
    public String getVSnapshotVISDKServer() {
        return vSnapshotVISDKServer;
    }

    public void setVSnapshotVISDKServer(String vSnapshotVISDKServer) {
        this.vSnapshotVISDKServer = vSnapshotVISDKServer;
    }

    // Getter und Setter für vSnapshotHost
    public String getVSnapshotHost() {
        return vSnapshotHost;
    }

    public void setVSnapshotHost(String vSnapshotHost) {
        this.vSnapshotHost = vSnapshotHost;
    }

    // Getter und Setter für import_date
    public String getImport_date() {
        return import_date;
    }

    public void setImport_date(String import_date) {
        this.import_date = import_date;
    }

    // Getter und Setter für upload_index
    public int getUpload_index() {
        return upload_index;
    }

    public void setUpload_index(int upload_index) {
        this.upload_index = upload_index;
    }
}
